SELECT
CASE
WHEN AGE < 30 THEN 'UNDER 30'
WHEN AGE BETWEEN 30 AND 40 THEN '30-40'
WHEN AGE BETWEEN 41 AND 50 THEN '41-50'
ELSE '50+'
END AS AGE_GROUP,
SUM(BALANCE) AS REVENUE_LOSS
FROM CUSTOMER_CHURN
WHERE EXITED = 1
GROUP BY CASE
WHEN AGE < 30 THEN 'UNDER 30'
WHEN AGE BETWEEN 30 AND 40 THEN '30-40'
WHEN AGE BETWEEN 41 AND 50 THEN '41-50'
ELSE '50+'
END;